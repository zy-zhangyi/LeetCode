package Medium;

public class atoi_8 {

    public static void main(String[] args) {
        int i = myAtoi("    43");
    }


    public static int myAtoi(String str) {
        String[] s = str.split(" ");
        for (String a : s)
        {
            System.out.println(a);
        }

        return 0;
    }
}
